# ╔═╡ b5a9db95-6126-4892-9a21-ee2b4c43924f
# Theoretical Speedup Given an input size
# Source: https://arxiv.org/pdf/1411.5373.pdf
begin
	# Hardcode current test pulse params (TODO: Make dynamic)
	n_f  = 4096
	n_t  = 3000
	n_dm = 2076
	
	function algo_cmplx_brute(n_f, n_t, n_dm)
		return n_f * n_t * n_dm
	end

	function algo_cmplx_fdmt(n_f, n_t, n_dm)
		return max(n_t * n_dm * log2(n_f), 2 * n_f * n_t)
	end

	function algo_cmplx_tree(n_f, n_t, n_dm)
		return n_f * n_t * log2(n_f)
	end
	
	cmplx_brute = algo_cmplx_brute(n_f, n_t, n_dm)
	cmplx_fdmt  = algo_cmplx_fdmt(n_f, n_t, n_dm)
	cmplx_tree  = algo_cmplx_tree(n_f, n_t, n_dm)

	algo_fdmt_speedup = cmplx_brute / cmplx_fdmt
	algo_tree_speedup = cmplx_brute / cmplx_tree
	nothing
end

